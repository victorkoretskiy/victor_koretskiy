#(рядок, список, кортеж) і повертає генератор, який буде вертати значення з цієї послідовності,
# при цьому, якщо було повернено останній елемент із послідовності - ітерація починається def yes_or_no():
def generator(obj):
    i=0
    while True:
        if i >=len(obj):
            i=0
        yield obj[i]
        i+=1
for elem in generator(['1','2','3']):
    print(elem)












