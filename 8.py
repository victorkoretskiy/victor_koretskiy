# 8. Написати функцію, яка буде реалізувати логіку циклічного зсуву елементів в списку. Тобто, функція приймає два аргументи:
# список і величину зсуву (якщо ця величина додатня - пересуваємо з кінця на початок, якщо від'ємна - навпаки -
# пересуваємо елементи з початку списку в його кінець).
def shift_list(list_1, shift):
    return list_1[-shift:] + list_1[:-shift]
